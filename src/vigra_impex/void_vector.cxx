#include "void_vector.hxx"

namespace vigra
{
    void swap_void_vector( void_vector_base & left, void_vector_base & right )
    {
        std::swap( left.m_data, right.m_data );
        std::swap( left.m_size, right.m_size );
        std::swap( left.m_capacity, right.m_capacity );
    }
}
